class A 
{
	public void hello1()
	{
		int x=0xb;//11
		System.out.println(x);
	}
	public void hello2()
	{
		int y=0xdfc;//3580
		System.out.println(y);
	}
	public void hello3()
	{
		int z=0X34bcf;//216015
		System.out.println(z);
	}
}
class Hexa_Decimal_Numbersystem
{
	public static void main(String args[])
	{
		A ao=new 	A();
		ao.hello1();
		ao.hello2();
		ao.hello3();
	}
}